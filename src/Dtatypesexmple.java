public class Dtatypesexmple {
    public static void main(String arg[])
    {
        // datatypes example
        int a=100;
        float b=20;
        double c=5.555;
        char d='r';
        short e=5;
        boolean f=true;
        String g="Primitive DataTypes";

        System.out.println(" Integer Value : " +a);
        System.out.println(" Float Value : " +b);
        System.out.println(" Double Value : " +c);
        System.out.println(" Char Value : " +d);
        System.out.println(" Short Value : " +e);
        System.out.println(" Boolean Value : " +f);
        System.out.println(" String Value : " +g);




    }
}
